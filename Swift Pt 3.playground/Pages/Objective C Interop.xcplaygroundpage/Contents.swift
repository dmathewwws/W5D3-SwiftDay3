
//: ## Interacting with Objective C
//: To call Objective-C from Swift:
//: 
//: 1. Create a "bridging header"
//: 2. In it, `#import` any ObjC headers you want to use in Swift.
//: 3. Done!

//: To call Swift from Objective-C:
//:
//: 1. Import the "module header"
//: 2. Import the header in your ObjC Code.
//: 3. Done! (except if you're using an idea that can't be represented in ObjC).




